package org.Lesson303_2_1.OperatorsAndType;

public class UnaryOperator7 {
    public static void main(String[] args)
    {
        int sum = +1;
        System.out.println(sum);

        sum--;
        System.out.println(sum);

        sum++;
        System.out.println(sum);

        sum = -sum;
        System.out.println(sum);

        boolean result = false;
        System.out.println(result);
        System.out.println(!result);
    }
}
