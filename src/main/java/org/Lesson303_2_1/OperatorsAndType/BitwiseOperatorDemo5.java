package org.Lesson303_2_1.OperatorsAndType;

public class BitwiseOperatorDemo5 {
    public static void main(String[] args)
    {
        int x = 58;
        int y =13;
        System.out.println("x & y : " + (x & y));
        System.out.println("x | y : " + (x | y));
        System.out.println("x ^ y : " + (x ^ y));
        System.out.println("~x : " + (~x));
        System.out.println("x << y : " + (x << y));
        System.out.println("x >> y : " + (x >> y));
    }
}

